package for_while_loop_exercises;

public class ForSicakliklar {
    public static void main(String[] args) {
        //For dongusu kullanarak 50 ile 60 dereceleri arasindaki sicaklik olculerini
        // fahrenheit cinsinden ekrana yazdiriniz.






















        for (int i = 50; i <=60; i++) {
            double f = i*1.8+32;
            System.out.println(i+"derece = "+ f + "fahrenayt");
        }


    }
}
