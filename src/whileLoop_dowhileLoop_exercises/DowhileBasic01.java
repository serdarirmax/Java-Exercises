package whileLoop_dowhileLoop_exercises;

public class DowhileBasic01 {
    public static void main(String[] args) {
        // 5 ten 3 e kadar tam sayilari konsola yazdiriniz.



























        int i = 5;
        do {
            System.out.println(i);
            i--;
        }while (i>3);
        System.out.print(i+" ");
    }
}
