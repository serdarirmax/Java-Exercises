package for_loop_exercises;

public class SayilarinTplmi {
    public static void main(String[] args) {
        // 5 den 8 e kadar tamsayilarin toplamini veren kodu yaziniz. (for ile.)
        // Ornek:   5+6+7+8 ==> 26



























        int sum = 0;
        for (int i = 5; i <9; i++) {
            sum+=i;
        }
        System.out.println(sum);

    }
}
