package for_loop_exercises;
public class ForBasic {
    public static void main(String[] args) {

        // 3 ile 9 arasindaki tam sayilarin carpimini bulmak icin kodu yaziniz.
        // 3*4*5*6*7*8*9 ==> 181.440


























        int multiply = 1;
        for (int i = 3; i <10; i++) {
            multiply*=i;
        }
        System.out.println(multiply);
    }
}
