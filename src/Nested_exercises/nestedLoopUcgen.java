package Nested_exercises;

public class nestedLoopUcgen {
    public static void main(String[] args) {

        int i=10;
        int j=1;

        while (i>=1){
            j=1;
            while (j<=i){
                System.out.print("*");
                j++;
            }
            System.out.println("");
            i--;


        }





    }
}


